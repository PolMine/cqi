hexToRaw <- function(x){
  xHexs <- format(as.hexmode(unlist(x)), width=4)
  xAsRaw <- lapply(
    xHex,
    function(xHex){
      c(
        as.raw(as.hexmode(substr(xHex, start=1, stop=2))),
        as.raw(as.hexmode(substr(xHex, start=3, stop=4)))
      )
  })
  names(xAsRaw) <- names(x)
  xAsRaw
}

